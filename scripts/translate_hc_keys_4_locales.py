def apply_locale(path: Path, translations: dict[str, str]) -> int:
    data = load(path)
    changed = 0
    for k, v in list(data.items()):
        if not k.startswith("hc_"):
            continue
        if k in translations and data.get(k) != translations[k]:
            data[k] = translations[k]
            changed += 1
    if changed:
        save(path, data)
    return changed


